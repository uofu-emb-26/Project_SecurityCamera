// For this function to work, buffer16 must be stored in RAM (it must not be a const)
void ili9341_array_endian_swap(uint16_t *buffer16, uint32_t num_pixels)
{
    uint32_t *buffer32 = (uint32_t *)buffer16;

    // Perform the endian swap on every two pixels
    for (uint32_t i = 0; i < (num_pixels / 2); i++)
    {
        // Flips A, B, C, D to B, A, D, C (endian-swap on 2-byte values)
        buffer32[i] = __REV16(buffer32[i]);
    }

    // Endian-swap on the final pixel, if needed
    if ((num_pixels % 2) != 0)
    {
        buffer16[num_pixels - 1] = __REV16(buffer16[num_pixels - 1]);
    }
}